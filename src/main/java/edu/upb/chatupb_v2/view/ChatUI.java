    private void filtrarContactos() {
        String filtro = jtBuscadorContactos.getText().toLowerCase().trim();
        contacModel.clear();
        if (filtro.isEmpty()) {
            for (Contact c : todosLosContactos) {
                contacModel.addElement(c);
            }
        } else {
            for (Contact c : todosLosContactos) {
                if (c.getName() != null && c.getName().toLowerCase().contains(filtro)) {
                    contacModel.addElement(c);
                }
            }
        }
        jContactos.repaint();
    }
